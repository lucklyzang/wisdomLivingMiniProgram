<template>
    <view class="light-hint" v-if="isShow">
        <image :src="type == 'success' ? passedPng : exclamationPointPng"></image>
        <text>{{message}}</text>
    </view>
</template>
 
<script>
export default {
  name: 'LightHint',
  props: {
    type: {
      type: String,
      default: "success"
    },
    message: {
      type: String,
      default: ""
    },
    duration: {
      type: Number,
      default: 3000 //默认3000毫秒消失
    }
  },
  data() {
    return {
      exclamationPointPng: require("@/static/img/exclamation-point.png"),
      passedPng: require("@/static/img/passed.png"),
      isShow: false //是否显示标识
    };
  },
  methods: {
    show() {
      this.isShow = true;
      setTimeout(this.hide, this.duration);//设置时间自动消失
    },
    hide() {
      this.isShow = false;
      this.remove();//移除
    }
  }
};
</script>
